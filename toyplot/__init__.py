from __future__ import division

__version__ = "0.7.0"

from toyplot.canvas import Canvas


def bars(
        a,
        b=None,
        c=None,
        along="x",
        baseline="stacked",
        fill=None,
        colormap=None,
        palette=None,
        opacity=1.0,
        title=None,
        style=None,
        xmin=None,
        xmax=None,
        ymin=None,
        ymax=None,
        show=True,
        xshow=True,
        yshow=True,
        label=None,
        xlabel=None,
        ylabel=None,
        xscale="linear",
        yscale="linear",
        padding=10,
        width=None,
        height=None,
        canvas_style=None):
    """Convenience function for creating a bar plot in a single call.

    See :meth:`toyplot.axes.Cartesian.bars`, :meth:`toyplot.canvas.Canvas.axes`, and :class:`toyplot.canvas.Canvas` for parameter descriptions.

    Returns
    -------
    canvas: :class:`toyplot.canvas.Canvas`
      A new canvas object.
    axes: :class:`toyplot.axes.Cartesian`
      A new set of 2D axes that fill the canvas.
    mark: :class:`toyplot.mark.BarMagnitudes` or :class:`toyplot.mark.BarBoundaries`
      The new bar mark.
    """
    canvas = Canvas(width=width, height=height, style=canvas_style)
    axes = canvas.axes(
        xmin=xmin,
        xmax=xmax,
        ymin=ymin,
        ymax=ymax,
        show=show,
        xshow=xshow,
        yshow=yshow,
        label=label,
        xlabel=xlabel,
        ylabel=ylabel,
        xscale=xscale,
        yscale=yscale,
        padding=padding)
    mark = axes.bars(
        a=a,
        b=b,
        c=c,
        along=along,
        baseline=baseline,
        fill=fill,
        colormap=colormap,
        palette=palette,
        opacity=opacity,
        title=title,
        style=style)
    return canvas, axes, mark


