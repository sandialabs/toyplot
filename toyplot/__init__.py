def matrix(
        matrix,
        label=None,
        step=1,
        colormap=None,
        palette=None,
        width=None,
        height=None,
        canvas_style=None):
    """Convenience function to create a matrix visualization in a single call.

    See :meth:`toyplot.canvas.Canvas.matrix`, and :class:`toyplot.canvas.Canvas` for parameter descriptions.

    Returns
    -------
    canvas: :class:`toyplot.canvas.Canvas`
      A new canvas object.
    table: :class:`toyplot.axes.Table`
      A new set of table axes that fill the canvas.
    """
    canvas = Canvas(width=width, height=height, style=canvas_style)
    table = canvas.matrix(
        matrix=matrix,
        label=label,
        step=step,
        colormap=colormap,
        palette=palette)
    return canvas, table


def table(
        data=None,
        rows=None,
        columns=None,
        hrows=None,
        brows=None,
        lcols=None,
        rcols=None,
        label=None,
        width=None,
        height=None,
        canvas_style=None):
    """Convenience function to create a table visualization in a single call.

    See :meth:`toyplot.canvas.Canvas.table`, and :class:`toyplot.canvas.Canvas` for parameter descriptions.

    Returns
    -------
    canvas: :class:`toyplot.canvas.Canvas`
      A new canvas object.
    table: :class:`toyplot.axes.Table`
      A new set of table axes that fill the canvas.
    """
    canvas = Canvas(width=width, height=height, style=canvas_style)
    table = canvas.table(
        data=data,
        rows=rows,
        columns=columns,
        hrows=hrows,
        brows=brows,
        lcols=lcols,
        rcols=rcols,
        label=label)
    return canvas, table
